const paisa = require('./index')

const assert = require('assert')
const mocha = require('mocha')

mocha.describe('paisa', function () {
  mocha.it('should do simple formatting', function () {
    assert.equal('3.34', paisa.format(334))
    assert.equal('9,723.34', paisa.format(972334))
    assert.equal('792,83,83,393.34', paisa.format(792838339334))
    assert.equal('92,83,83,393.34', paisa.format(92838339334))
    assert.equal('2,83,83,393.34', paisa.format(2838339334))
    assert.equal('83,83,393.34', paisa.format(838339334))
    assert.equal('3,83,393.34', paisa.format(38339334))
    assert.equal('83,393.34', paisa.format(8339334))
    assert.equal('393.34', paisa.format(39334))
    assert.equal('0.03', paisa.format(3))
    assert.equal('0.50', paisa.format(50))
    assert.equal('0.00', paisa.format(0))
    assert.equal('393.3', paisa.format(39334, 1))
    assert.equal('393', paisa.format(39334, 0))
    assert.equal('0', paisa.format(0, 0))
  })

  mocha.it('should do formatting with symbol', function () {
    assert.equal('₹3.34', paisa.formatWithSymbol(334))
    assert.equal('₹9,723.34', paisa.formatWithSymbol(972334))
    assert.equal('₹2', paisa.formatWithSymbol(200, 0))
  })

  mocha.it('should convert to words', function () {
    assert.equal('forty two paise', paisa.words(42))
    assert.equal('two paise', paisa.words(2))
    assert.equal('ten paise', paisa.words(10))
    assert.equal('fifteen paise', paisa.words(15))
    assert.equal('ninety two paise', paisa.words(92))
    assert.equal('two rupees, ninety two paise', paisa.words(292))
    assert.equal('twenty eight rupees, thirty seven paise', paisa.words(2837))
    assert.equal('nine hundred and twenty three rupees, forty eight paise', paisa.words(92348))
    assert.equal('eight thousand, four hundred and sixty two rupees, twenty seven paise', paisa.words(846227))
    assert.equal('fifty seven thousand, two hundred and two rupees, seventy four paise', paisa.words(5720274))
    assert.equal('seven lakh, seventy five thousand, one hundred and ninety three rupees, eighty five paise', paisa.words(77519385))
    assert.equal('sixty seven lakh, seventy five thousand, one hundred and ninety three rupees, eighty five paise', paisa.words(677519385))
    assert.equal('eight crore, sixty seven lakh, seventy five thousand, one hundred and ninety three rupees, eighty five paise', paisa.words(8677519385))
    assert.equal('eighteen crore, sixty seven lakh, seventy five thousand, one hundred and ninety three rupees, eighty five paise', paisa.words(18677519385))
    assert.equal('seven hundred and eighteen crore, sixty seven lakh, seventy five thousand, one hundred and ninety three rupees, eighty five paise', paisa.words(718677519385))
    assert.equal('three hundred rupees', paisa.words(30000))
    assert.equal('fifty thousand rupees', paisa.words(5000000))
    assert.equal('five lakh rupees', paisa.words(50000000))
    assert.equal('five crore rupees', paisa.words(5000000000))
    assert.equal('five crore, forty two lakh rupees', paisa.words(5420000000))
    assert.equal('five crore, forty two lakh rupees, seventy four paise', paisa.words(5420000074))
    assert.equal('five hundred crore, forty two lakh rupees, seventy four paise', paisa.words(500420000074))
  })
})
